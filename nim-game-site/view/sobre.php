							<h4 class="header"><blockquote>A história por trás do jogo</blockquote></h4>
							<p class="grey-text text-darken-3 lighten-3 text-justify">
								Incentivados pelo ambiente criativo, bem como pela abundância da matéria-prima, muitas escolas utilizam jogos educativos para desenvolver a capacidade de raciocínio. Vamos conhecer um deles: o NIM.<br>
								O NIM é um jogo bastante simples de jogar, embora exija raciocínio matemático e geométrico bastante razoável. A origem exata desse jogo não é muito conhecida, sendo porém jogado desde a antiguidade. As primeiras referências europeias a NIM vão desde o início do século XVI. Seu nome atual foi cunhado por Charles L. Bouton, da Universidade de Harvard, que também desenvolveu a teoria completa do jogo em 1901. Outro estudioso a respeito desse jogo foi o americano Martin Gardner, que descreveu o NIM em seu livro “Divertimentos Matemáticos”. Segundo Gardner, o NIM surgiu há mais de 2 mil anos na China antiga. Um de seus registros históricos (computacionalmente falando) data Estados Unidos 1940, quando foi patenteada uma máquina chamada NIMATRON, que era instalada em bares e cassinos desafiando as pessoas a jogar NIM contra a máquina.  Outro registro histórico famoso foi em 1951, no Festival Britânico, onde foi exibido um robô chamado Nimrod, que servia exclusivamente para jogar NIM com os visitantes da feira.<br>
								O NIM é jogado com palitos de dente ou de fósforos ou até mesmo moedas, e começa com fileiras com os palitos, uma embaixo da outra. A quantidade de fileiras pode ser variável, assim como a quantidade de jogadores.<br>
								Cada jogador tem direito a tirar quantos palitos quiser, mas somente de uma fileira de cada vez. Assim, pode-se tirar um único palito da fileira de 4, ou todos da fileira de 3, ou dois da fileira de 5, desde que a mesma seja válida e contenha quantidade de palitos suficiente, passando então a vez ao adversário.<br>
								Nim normalmente é jogado como um jogo misère, no qual o jogador que tira o último objeto perde. Também pode ser jogado como um jogo de jogo normal, onde o jogador que tira o último objeto ganha. Não há uma única disposição possível para os palitos, existindo variações a respeito. Contudo, em todas elas a regra básica é mesma.<br>
								Pode parecer fácil, mas, segundo Martin Gardner, existem milhões de possibilidades de sequências de jogo. Gardner, em seu livro, também mostra algumas fórmulas matemáticas e cálculos binários que garantem a vitória a quem tira o primeiro palito. É exatamente aí que reside a diversão deste jogo. Todos jogadores acham que vão ganhar, mas, a qualquer momento, podem ser surpreendidos por seus oponentes e perder de surpresa.
